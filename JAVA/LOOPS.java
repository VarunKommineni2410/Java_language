public class LOOPS {
    public static void main(String[] args) {

        //while

        int i = 0;
        while (i != 10) {
            System.out.println(i);
            i++;
        }

        //For

        int a=20;
        for(int k=10;k<=a;k++){
            System.out.println(k);
        }

        //Do While

        int b = 21;
        do{
            System.out.println(b);
            b++;
        }while (b!=31);

    }
}